package main;

public class OperatorsMain {

    public static void main(String[] args) {

        // integers division
        int a = 7;
        int b = 5;

        int res = a / b;
        System.out.println("int division. 7 / 5 = " + res);

        // floating point numbers division
        double x = 7;
        double y = 5;

        double res2 = x  / y;
        System.out.println("double division. 7 / 5 = " + res2);

    }
}
